//Assignment Operators

// = (assign)

let x = 15;
let y = 10;
console.log("x = ", x, "y = ", y);

// (+=)

let z = (x += y);
console.log("x += y", z);

// (-=)

let a = (x -= y);
console.log("x -= y", a);

// (*=)

let b = (x *= y);
console.log("x *= y", b);

// (/=)

let c = (x /= y);
console.log("x /= y", c);

// (%=)

let d = (x %= y);
console.log("x %= y", d);

// (**=)

let e = (x **= y);
console.log("x**=y", e);
